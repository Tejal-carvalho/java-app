package com.techlab.student;

public class Student {
	private int rollno;
	private String name;
	
	public int getRollno() {
		return rollno;
	}
	public String getName() {
		return name;
	}
	public void setRollno(int newRollNo) {
		rollno=newRollNo;
	}
	public void setName(String newName) {
		name=newName;
	}

}
